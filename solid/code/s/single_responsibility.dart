

//////// Single Responsibility Principle (SRP)
// A class should have only one reason to change.

// 🔍 Better readability: 
//      	Classes and functions are focused, making them easier to understand.

// 🛠 Easier to maintain
//       	When requirements change, you only update a small part of the code.

// ♻️ Better reusability
//      	Smaller, focused classes can be reused in different parts of the app.

// 🔄 Simplifies testing
//       	Unit tests are easier because each class has fewer dependencies and responsibilities.

// 👥 Improves team collaboration	
//        Different team members can work on different parts without stepping on each other's toes.